<h4 class="style4">&nbsp;</h4>
<h4 class="style4"><span class="style6">Effects </span>
  <hr />
</h4>
<p align="justify" class="style4">Uncontrolled   diabetes can be extremely dangerous, resulting in loss of vision, damage to the   nerves, blood vessels, kidney and increased risk of heart attack. Diabetes cannot   be cured but it can be controlled either by diet alone, oral medication or by   administering insulin. Some complications of diabetes are hard to control and   can even lead to premature death.</p>
<p align="justify" class="style4"><strong>Adverse effects</strong>  </p>
</p>
<ul class="style4">
  <li>
    <p align="justify"> High levels of glucose in the blood causes diabetic neuropathy. It can cause damage   to the blood vessels supplying the nerves and the nerves itself causing gradual   loss of sensation primarily in hands and feet which may extend up the limbs.</p>
  </li>
  <li>
    <p align="justify">It   causes damage to the blood vessels in the light-sensitive retina at the back of   the eye, which affects both the eyes and if left untreated can result in loss   of vision and blindness. People with diabetes should have their eye examined yearly. </p>
  </li>
  <li>
    <p align="justify">Damage   to the blood vessels in the kidneys and destroys the kidneys function of removing   wastes and excess water from the body. Symptoms include vomiting, breathlessness,   drowsiness etc.</p>
  </li>
  <li>
    <p align="justify">It can cause hypoglycemia (a disorder   in which the blood sugar falls to abnormally low levels) by the gradual build   up of glucose lowering drugs in the blood or excessive dose of insulin in relation   to food intake. New born baby of a diabetic mother can also develop hypoglycemia.</p>
  </li>
  <li>
    <p align="justify"> Bacteria thrives on glucose rich blood and urine, people with uncontrolled diabetes   are more prone to urinary tract infection.<br />
      It can cause chemical imbalance   in the blood causing nausea, vomiting, abdominal pain, confusion etc. It effects   type I diabetes patients. </p>
  </li>
</ul>
<h1 align="left" class="style4">&nbsp;</h1>
<h1 align="left" class="style4"><span class="style6">Treatment
</span>
<hr /></h1>
<p align="justify" class="style4">Diabetes cannot be cured but can be  controlled either by diet alone, or a combination of diet or oral medication or  by administering insulin injections. Treatment for diabetes is designed to control glucose  level in the blood with in the normal range. </p>
<p align="justify" class="style4">&nbsp;</p>
