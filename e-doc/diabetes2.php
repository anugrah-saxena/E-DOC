<h4 class="style4">&nbsp;</h4>
<h4 class="style4"><span class="style6">Exercises
  
  </span>
  <hr />
</h4>
<p align="justify" class="style4"> Regular exercise should   be an integral part of the treatment plan for persons with diabetes. Exercise   can help to <a >control your weight</a>, improve insulin sensitivity, reduce cardiovascular   risk factors and lower your blood sugar level. Exercise can also help you feel   better about yourself and increase your overall health. With appropriate   guidelines and taking certain precautions, people with diabetes can exercise safely. </p>
<p align="justify" class="style4">&nbsp; </p>
