<span class="style4"><strong>Type I diabetes :</strong> This usually  requires life-long insulin therapy along with the control in diet. This replaces the insulin absent in the body  due to non production of insulin by the pancreas. This  insulin therapy maintains constant low background levels of insulin with  peaks at mealtimes when glucose enters the blood. Insulin injections are available in  various forms including short-acting, long-acting and combination of both forms.  Short-acting insulin are take 15-30 minutes before mealtimes while  longer-acting insulin are taken once or twice a day. 
</p>
</span>
<p align="justify" class="style4"><strong>Type II diabetes :</strong> The  blood-glucose level can be controlled by exercising regularly and following a  healthy diet to maintain ideal weight. Sometimes the drugs may be needed to  stimulate the pancreas to produce more insulin or control your blood sugar. You may be  prescribed anti- diabetic drugs such as sulfonylureas, which stimulate the  pancreas to release insulin or metformin, which helps body tissues to absorb  glucose. You may also be given acarbose, which slows the absorption of glucose  from the intestine and prevents fluctuations in the blood level. If antidiabetic  drugs do not prove to be effective, insulin may be used to treat. </p>
