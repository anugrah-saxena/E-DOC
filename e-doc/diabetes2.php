<h4 class="style4">&nbsp;</h4>
<h4 class="style4"><span class="style6">Care and Diet 
  
  </span>
  <hr />
</h4>
<p align="justify" class="style4">Healthy routine helps to minimize the risk   of developing complications of diabetes. One can keep blood glucose levels normal   by a healthy diet and maintaining physical fitness. Eating right, controlling   your weight and exercising regularly will help you to control sugar level. </p>
<p align="justify" class="style4">&nbsp;</p>
