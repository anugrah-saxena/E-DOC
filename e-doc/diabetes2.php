<span class="style4">Excess consumption of <a  >alcohol</a> is harmful as it may lower blood glucose levels   and cause weight gain. Smoking is very harmful to diabetes patients, because it   increases the risk of long-term complications. Foot care is important because   diabetes can increase the risk of skin infections and ulcers on the feet. You   must clean your feet daily and if a sore develop on your feet, consult the doctor   promptly. Diabetes patients have an increased risk of high blood pressure, so   regular monitoring is important.
</p>
</span>
<p align="justify" class="style4">Low-fat, vegetarian diets   are ideal for diabetics. In most cases, diabetics can manage their disease much   better with a food plan that gets most of its calories from complex carbohydrates   while minimizing fats.</p>
<p align="justify" class="style4"><strong>Diet for Diabetes </strong>: </p>
<p align="justify" class="style4">With   a near-vegetarian <a >diet and exercise</a> program, one can maintain their sugar levels   with out taking any medications. Ideal diet program for a diabetic is a <a>low fat</a> vegetarian diet, reducing meats, high-fat dairy products, and oils. At the same   time increasing grains, legumes, and vegetables.  </p>
<ul class="style4">
  <li>
    <p align="justify">Have your meals at regular timings   and limit your overall intake.</p>
  </li>
  <li>
    <p align="justify"> Eat five servings   of fruits and vegetables every day. Soluble fibers mainly found in fruits, vegetables   and some seeds are good for diabetics. They reduce or slow down the absorption   of glucose from the intestines.<br />
      Diabetes patients should include leafy vegetables,   vegetable soups and juices in their diet. Vegetables like tomato, cucumber, bitter   gourd, carrot, cooked kidney beans, pumpkin, snake gourd, beans,  cabbage,   lady finger, brinjal, onion, spinach, inner part of banana stem, drum sticks etc.   are good for diabetes.   Fruits and fruit juices of  apple, guava,   lemon etc. are good. Avoid fruits like banana, mango, grapes, papaya, jack fruit   etc. and vegetables like potato, tapioca, sweet potato etc. containing lot of   carbohydrates.</p>
  </li>
  <li>
    <p align="justify">Eat more starches such as bread,   cereal, and starchy vegetables. Aim for six servings a day or more.</p>
  </li>
  <li>
    <p align="justify">Eat   sugars, sweets and fat and fat-fried items in moderation. <br />
      It is better   to avoid cakes, jaggery, honey, ghee, butter, cool drinks, biscuits, dried fruits   like badam and excess use of oil while cooking. </p>
  </li>
</ul>
