</table>
<span class="style4">The exercise plan will vary depending on interest, age, general health, level   of physical fitness, the type of diabetics and health problems related with diabetics.   Exercise is safest and most effective for people with type 2 diabetes. Aerobic   exercise is mostly recommended for diabetic patients. It helps to control diabetics   directly, by improving your body's use of sugar as well as indirectly by facilitating   weight loss. Examples of aerobic exercise include walking, jogging, aerobic dance   or bicycling. If you have problems with the nerves in your feet or legs, exercises   that won't put stress on your feet, like swimming, bicycling, rowing or chair   exercises are recommended. Regular, vigorous exercise helps insulin work   optimally and regulates sugar level.
</p>
</span>
<p align="justify" class="style4">Simple relaxation techniques   such as deep breathing exercises for 10-15 minutes every day can reduce blood   sugar level by 9 to 12 percent.</p>
<p align="justify" class="style4"><strong>Precautions essential   for exercise </strong></p>
<ul class="style4">
  <li>
    <p align="justify">Talk to your doctor about the   right exercise for you.</p>
  </li>
  <li>
    <p align="justify">Make sure your blood sugar   level is reasonably well controlled before you start an exercise program. If your   blood sugar level is too low or too high right before you plan to exercise, it's   better to wait until the level improves.</p>
  </li>
  <li>
    <p align="justify">Check your blood sugar level before and after exercising, especially in extremely hot or cold conditions.</p>
  </li>
  <li>
    <p align="justify">Check your feet for blisters, sores, redness or swelling before and after exercising.</p>
  </li>
  <li>
    <p align="justify">Drink plenty of fluid before, during and after exercising in order to avoid dehydration.</p>
  </li>
  <li>
    <p align="justify">Warm up before exercising and cool down afterward.</p>
  </li>
  <li>
    <p align="justify">Have  a sweet or juice handy in case your blood sugar level drops. It may be  gradual and you may feel shaky or hungry, suddenly sweat more and feel  a change in the heartbeat.</p>
  </li>
  <li>
    <p align="justify">Don't lift heavy weights. Straining raises your blood pressure and can damage blood vessels.</p>
  </li>
  <li>
    <p align="justify">Exercise should be done repeatedly at regular intervals.</p>
  </li>
</ul>
