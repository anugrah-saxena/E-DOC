    <td width="86%"><p class="style21"><strong>Swine influenza</strong> (also called <em>pig influenza, swine flu, hog flu </em>and <em>pig flu</em>) is an infection by any one of several types of swine influenza virus. <em>Swine influenza virus (SIV)</em> or <em>S-OIV (swine-origin influenza virus</em>) is any strain of the influenza family of viruses that is endemic in pigs. As of 2009, the known SIV strains include influenza C and the subtypes of influenza A known as <em>H1N1,H1N2,H3N1,H3N2</em> and <em>H2N3.</em></p>
