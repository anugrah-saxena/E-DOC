<h2 align="left" class="style11">&nbsp;</h2>
<h2 align="left" class="style11">Prevention
  <hr />
</h2>
<p align="left" class="style23"><span class="style27">Prevention of swine influenza has three components </span>:</p>
<p align="left" class="style30"> <span class="style17">(1).Prevention in  swine</span>.</p>
<p align="left" class="style26">Methods of preventing the spread of influenza among swine include facility management, herd management, and vaccination (<a href="http://en.wikipedia.org/wiki/ATCvet" title="ATCvet">ATCvet</a> code: <a href="http://en.wikipedia.org/wiki/ATCvet_code_QI09" title="ATCvet code QI09">QI09</a><a href="http://www.whocc.no/atcvet/atcvet_index/?code=QI09AA03" rel="nofollow">AA03</a>).  Because much of the illness and death associated with swine flu  involves secondary infection by other pathogens, control strategies  that rely on vaccination may be insufficient.Standard commercial swine flu vaccines are effective in controlling the  infection when the virus strains match enough to have significant  cross-protection, and custom (autogenous) vaccines made from the  specific viruses isolated are created and used in the more difficult  cases.Facility management includes using disinfectants and ambient  temperature to control virus in the environment.</p>
<p align="left" class="style30"><span class="style17">(2). Prevention of transmission to humans</span>.</p>
<p align="left" class="style27"><span class="style21">The transmission from swine to human is believed to occur mainly in  swine farms where farmers are in close contact with live pigs. Although  strains of swine influenza are usually not able to infect humans this  may occasionally happen, so farmers and veterinarians are encouraged to  use a face mask when dealing with infected animals. The use of vaccines on swine to  prevent their infection is a major method of limiting swine to human  transmission. Risk factors that may contribute to swine-to-human  transmission include smoking and, especially, not wearing gloves when  working with sick animals -- thereby increasing the likelihood of  subsequent hand-to-eye, hand-to-nose or hand-to-mouth transmission.</span></p>
<p align="left" class="style22">&nbsp;</p>
<p align="left" class="style19"><span class="style29">(3). Prevention of its  spread among humans. </span>
<p class="style26">Influenza spreads between humans when infected people cough or  sneeze, then other people breathe in the virus or touch something with  the virus on it and then touch their own face.&quot;Avoid touching your eyes, nose or mouth. Germs spread this way.&quot;Swine flu cannot be spread by pork products, since the virus is not transmitted through food.The swine flu in humans is most contagious during the first five days  of the illness although some people, most commonly children, can remain  contagious for up to ten days. Diagnosis can be made by sending a  specimen, collected during the first five days for analysis.</p>
<h2 class="style21">&nbsp;</h2>
<h2 class="style24">Treatment</h2>
<h1 class="style14"><hr />
</h1>
<h3><span class="style25" id="In_swine_3">In swine</span></h3>
<p class="style21">As swine influenza is rarely fatal to pigs, little treatment beyond rest and supportive care is required.Instead veterinary efforts are focused on preventing the spread of the virus throughout the farm, or to other farms.Vaccination and animal management techniques are most important in  these efforts. Antibiotics are also used to treat this disease, which  although they have no effect against the influenza virus, do help  prevent bacterial pneumonia and other secondary infections in influenza-weakened herds.</p>
<h3><span class="style25" id="In_humans_3">In humans</span></h3>
<p class="style21">If a person becomes sick with swine flu, antiviral drugs can make  the illness milder and make the patient feel better faster. They may  also prevent serious flu complications. For treatment, antiviral drugs  work best if started soon after getting sick (within 2 days of  symptoms). Beside antivirals, supportive care at home or in hospital,  focuses on controlling fevers, relieving pain and maintaining fluid  balance, as well as identifying and treating any secondary infections  or other medical problems. The U.S. Centers for Disease Control and Prevention recommends the use of Tamiflu (oseltamivir) or Relenza   for the treatment and/or prevention of infection with swine influenza  viruses; however, the majority of people infected with the virus make a  full recovery without requiring medical attention or antiviral drugs.The virus isolates in the 2009 outbreak have been found resistant to amantadine and rimantadine.</p>
